# Python program to add two numbers

num1 = eval(input("Enter the First value: "))  # Prompt the user to enter the first value and store it in 'num1'
num2 = eval(input("Enter the second value: "))  # Prompt the user to enter the second value and store it in 'num2'
addition = num1 + num2  # Add the values of 'num1' and 'num2' and assign the result to 'addition'
print(addition)  # Print the result of the addition
