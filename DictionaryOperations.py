# Define a dictionary
students = {"Ahmad": "44", "Rana ": "55.6", "Zaid ": "75.8", "susan": 5}

# Print the initial dictionary
print(students)

# Add a new key-value pair to the dictionary
students["peter"] = 5
print(students)

# Update the value of an existing key
students["peter"] += 5
print(students)

# Remove a key-value pair from the dictionary
del students["peter"]
print(students)

print("\n")
