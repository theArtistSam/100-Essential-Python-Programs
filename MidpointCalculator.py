# Python program to Calculate Mid Point 

# Receive the first value as input
num1 = eval(input("Enter the first value: "))

# Receive the second value as input
num2 = eval(input("Enter the second value: "))

# Receive the third value as input
num3 = eval(input("Enter the third value: "))

# Receive the fourth value as input
num4 = eval(input("Enter the fourth value: "))

# Calculate the x-coordinate of the midpoint
MidpointX = (num1 + num2) / 2

# Calculate the y-coordinate of the midpoint
MidpointY = (num3 + num4) / 2

# Print the x-coordinate of the midpoint
print("Midpoint X-coordinate:", MidpointX)

# Print the y-coordinate of the midpoint
print("Midpoint Y-coordinate:", MidpointY)
