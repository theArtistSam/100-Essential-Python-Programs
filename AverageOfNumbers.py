# Python program to calculate average of three numbers

# Prompt the user to enter three values
num1 = eval(input("Enter the First value: "))
num2 = eval(input("Enter the second value: "))
num3 = eval(input("Enter the third value: "))

# Calculate the average of the three values
Average = (num1 + num2 + num3) / 3

# Print the average
print("The average is:", Average)
