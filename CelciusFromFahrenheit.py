# Python program to calculate the Fahrenheit Temperature 

# Prompt the user to enter a value for Fahrenheit temperature
ValueFahrenheit = eval(input("Enter the value of Fahrenheit temperature: "))

# Convert the Fahrenheit temperature to Celsius using the conversion formula
Celcius = 5 / 9 * (ValueFahrenheit - 32)

# Print the converted Celsius temperature
print("The temperature in Celsius is:", Celcius)
