# Python program to calculate the Area of a Rectangle

length = eval(input("Enter the value of length: "))  # Prompt the user to enter the value of length and store it in 'length'
width = eval(input("Enter the value of width: "))  # Prompt the user to enter the value of width and store it in 'width'
Area = length * width  # Calculate the area by multiplying length and width, and assign the result to 'Area'
print(Area)  # Print the calculated area
