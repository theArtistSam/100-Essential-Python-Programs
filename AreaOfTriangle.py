# Python program to calculate the Area of Triangle

width = eval(input("Enter the value of width: "))  # Prompt the user to enter the value of width and store it in 'width'
height = eval(input("Enter the value of height: "))  # Prompt the user to enter the value of height and store it in 'height'
Area = 1/2 * (width * height)  # Calculate the area using the formula and assign the result to 'Area'
print(Area)  # Print the calculated area
